	public String getStyleRules() {
		DText textNode = (DText)super.getFirstChild();
		if (textNode == null) {
			return null;
		}
		return textNode.getData();
	}
	
	public DStyle setStyleRules(final String text) {
		DText textNode = (DText)super.getFirstChild();
		if (textNode == null) {
			textNode = new DText(text);
			add(textNode);
		} 
		else {
			textNode.setData(text);			
		}
		return this;
	}

